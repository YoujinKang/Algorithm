import sys
input = sys.stdin.readline

n = int(input())  # 1 <= n <= 90

d = [0] * (n + 1) 

# 첫 번째 자리 = 1, 두 번째 자리 = 0이 정해져있음
# 1 : 1
# 2 : 10
# 3 : 10/0, 10/1
# 4 : 10/00, 10/01, 10/10
# 5 : 10/000, 10/001, 10/010, 10/100, 10/101
# 6 : 10/0000, 10/0001, 10/0010, 10/0100, 10/1000, 10/0101, 10/1010
# d[i] = d[i - 2] + d[i - 1]

for i in range(1, n + 1): 
    if i == 1 or i == 2:
        d[i] = 1  
    else:  
        d[i] = d[i - 1] + d[i - 2]

print(d[n])
